#include "3-calc.h"

/**
 * op_add - adds arguments
 * @a: first interger
 * @b: second integer
 * Return: add
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts arguments
 * @a: first interger
 * @b: second integer
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies arguments
 * @a: first interger
 * @b: second integer
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides arguments
 * @a: first interger
 * @b: second integer
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - gets modulus
 * @a: first arguments
 * @b: second arguments
 * Return: modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
